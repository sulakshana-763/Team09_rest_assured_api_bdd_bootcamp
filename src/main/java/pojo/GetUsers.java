package pojo;

public class GetUsers {
	
	private GetAddress userAddress; 
	private String user_first_name; 
	private String user_last_name; 
	private int user_contact_number; 
	private String user_email_id;
	private int user_id;
	private String creation_time;
	private String last_mod_time;
	private String status;
	private String message;
	
	public GetAddress getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(GetAddress userAddress) {
		this.userAddress = userAddress;
	}
	public String getUser_first_name() {
		return user_first_name;
	}
	public void setUser_first_name(String user_first_name) {
		this.user_first_name = user_first_name;
	}
	public String getUser_last_name() {
		return user_last_name;
	}
	public void setUser_last_name(String user_last_name) {
		this.user_last_name = user_last_name;
	}
	public int getUser_contact_number() {
		return user_contact_number;
	}
	public void setUser_contact_number(int user_contact_number) {
		this.user_contact_number = user_contact_number;
	}
	public String getUser_email_id() {
		return user_email_id;
	}
	public void setUser_email_id(String user_email_id) {
		this.user_email_id = user_email_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getCreation_time() {
		return creation_time;
	}
	public void setCreation_time(String creation_time) {
		this.creation_time = creation_time;
	}
	public String getLast_mod_time() {
		return last_mod_time;
	}
	public void setLast_mod_time(String last_mod_time) {
		this.last_mod_time = last_mod_time;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	} 
		   
		 
}
